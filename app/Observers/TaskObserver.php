    /**
     * Handle the Task "updating" event.
     *
     * @param  \App\Models\Task  $task
     * @return void
     */
    public function updating(Task $task)
    {

        if ($task->isDirty('status') && $task->status == 'Completed') {
            $task->completed_at = now();
        }
    }

